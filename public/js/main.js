let day = prompt("Donnez un jour de la semaine:");

switch (day) {
    case "lundi":
        console.log("premier jour de confinement");
        break;

    case "mardi":
        console.log("deuxieme jour de confinement");
        break;

    case "mercredi":
        console.log("troisieme jour de confinement");
        break;

    case "jeudi":
        console.log("quatrieme jour de confinement");
        break;

    case "vendredi":
        console.log("cinquieme jour de confinement");
        break;

    case "samedi":
        console.log("sixieme jour de confinement");
        break;
    
    case "dimanche":
        console.log("septieme jour de confinement");
        break;

    default:
        break;
}